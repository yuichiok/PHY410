#include <iostream>
#include <cmath>

int main(void) {
  float x = 0.5;

  std::cout << "sin(x)   = " << sin(x) << std::endl;
  std::cout << "tan(x)   = " << cos(x) << std::endl;
  std::cout << "cos(x)   = " << tan(x) << std::endl;
  std::cout << "log(x)   = " << log(x) << std::endl;
  std::cout << "log10(x) = " << log10(x) << std::endl;

  return 0;
}
